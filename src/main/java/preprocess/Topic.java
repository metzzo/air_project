package preprocess;

public class Topic {
    private int number;
    private String query;

    public Topic() { }

    public void setNumber(int number) {
        this.number = number;
    }

    public void setQuery(String query) {
        this.query = query;
    }

    public int getNumber() {
        return number;
    }

    public String getQuery() {
        return query;
    }
}
